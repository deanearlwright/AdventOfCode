class Plan(object):   # pylint: disable=R0902, R0903, R0205
    "Object for Lavaduct Lagoon"

    def __init__(self, text=None, part2=False):

        # 1. Set the initial values
        self.part2 = part2
        self.text = text
        self.steps = []

        # 2. Process text (if any)
        if text is not None and len(text) > 0:
            for line in text:
                self.steps.append(Step(text=line, part2=part2))

    def __iter__(self):
        return iter(self.steps)

