    "Object for Dirac Dice"

    def __init__(self, text=None, part2=False):

        # 1. Set the initial values
        self.part2 = part2
        self.text = text
        self.number = 0
        self.position = 0
        self.score = 0
        self.wins = 0

        # 2. Process text (if any)
        if text is not None and len(text) > 0:
            parts = text.split()
            self.number = int(parts[1])
            self.position = int(parts[4]) - 1


