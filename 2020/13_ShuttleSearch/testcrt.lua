-- Test Chinese remainder theorem

-- ----------------------------------------------------------------------
--                                                                require
-- ----------------------------------------------------------------------
local luaunit = require('luaunit')

local crt = require('crt')

-- ----------------------------------------------------------------------
--                                                              constants
-- ----------------------------------------------------------------------

-- ======================================================================
--                                                                Testcrt
-- ======================================================================

function test_rosettacode()

  luaunit.assertEquals(crt.chineseRemainder({3, 5, 7}, {2, 3, 2}), 23)
  luaunit.assertEquals(crt.chineseRemainder({11, 12, 13}, {10, 4, 12}), 1000)
  luaunit.assertEquals(crt.chineseRemainder({100, 23}, {19, 0}), 1219)
  luaunit.assertEquals(crt.chineseRemainder({5, 13}, {2, 3}), 42)

end


-- ----------------------------------------------------------------------
--                                                  module initialization
-- ----------------------------------------------------------------------
os.exit( luaunit.LuaUnit.run() )

