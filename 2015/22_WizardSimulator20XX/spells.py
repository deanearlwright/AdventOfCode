import spell


# ----------------------------------------------------------------------
#                                                              constants
# ----------------------------------------------------------------------

SPELLS = [
    "Magic Missile, 53, 0, 4, 0, 0, 0",
    "Drain, 73, 0, 2, 2, 0, 0, 0",
    "Shield, 113, 6, 6, 0, 0, 7, 0",
    "Poison, 173, 6, 2, 0, 0, 0",
    "Recharge, 229, 5, 0, 0, 0, 101",
]

# ======================================================================
#                                                                 Spells
# ======================================================================


class Spells(object):   # pylint: disable=R0902, R0205
    "Object for Wizard Simulator 20XX"

    def __init__(self):

        # 1. Set the initial values
        self.spells = []

        # 2. Add in the spells
        for text in SPELLS:
            self.spells.append(spell.Spell(text=text))


