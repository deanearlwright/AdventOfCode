RE_PATH = re.compile(r"[0-9]+|[RL]")

# ======================================================================
#                                                                   Path
# ======================================================================


class Path(object):   # pylint: disable=R0902, R0903, R0205
    "Object for Monkey Map"

    def __init__(self, text=None, part2=False):

        # 1. Set the initial values
        self.part2 = part2
        self.text = text
        self.instructions = []

        # 2. Process text (if any)
        if text is not None and len(text) > 0:
            self.instructions = RE_PATH.findall(self.text)


